package EjerciciosSueltos;
public class EjerVarialbes
{

	public static void main(String[] args)
	{
		int numero = 1254;
		char caracter = 'F';
		float flotante = 1557f;
		double doble = 15656;
		byte bit = 14;
		short corto = 1547;
		long largo = 1547868468;
		boolean booleano = true;
		String cadena = "Hola Mundo";
		
		System.out.println("Variable Tipo INT "+numero);
		System.out.println("Variable Tipo Chart "+caracter);
		System.out.println("Variable Tipo float "+flotante);
		System.out.println("Variable Tipo double "+doble);
		System.out.println("Variable Tipo byte "+bit );
		System.out.println("Variable Tipo short "+corto);
		System.out.println("Variable Tipo long "+largo);
		System.out.println("Variable Tipo boolean "+booleano);
		System.out.println("Variable Tipo String "+cadena);
		
	}
}